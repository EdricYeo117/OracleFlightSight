  TOTAL_DWELL_MS            NUMBER DEFAULT 0 NOT NULL,
  FIXATION_COUNT            NUMBER DEFAULT 0 NOT NULL,
  LAST_UPDATED              TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,

  CONSTRAINT PK_HEATMAP_CELL_AGGREGATE
    PRIMARY KEY (SESSION_ID, GRID_X, GRID_Y),

  CONSTRAINT FK_HEATMAP_AGG_SESSION
    FOREIGN KEY (SESSION_ID) REFERENCES FLIGHT_SESSION(SESSION_ID)
);

CREATE TABLE AOI_RULE (
  RULE_ID                   VARCHAR2(64) PRIMARY KEY,
  SCENARIO_ID               VARCHAR2(100) NOT NULL,
  AOI                       VARCHAR2(64) NOT NULL,
  RULE_NAME                 VARCHAR2(200),
  RULE_MODE                 VARCHAR2(30) DEFAULT 'HYBRID' NOT NULL,
  MIN_DWELL_MS              NUMBER,
  MIN_FIXATION_COUNT        NUMBER,
  MIN_SUSTAINED_FIXATION_MS NUMBER,
  MAX_ALLOWED_GAP_MS        NUMBER,
  REQUIRED_ORDER_NO         NUMBER,
  IS_REQUIRED               NUMBER(1) DEFAULT 1 NOT NULL,
  DESCRIPTION               VARCHAR2(500),

  CONSTRAINT CHK_AOI_RULE_REQUIRED
    CHECK (IS_REQUIRED IN (0, 1)),

  CONSTRAINT CHK_AOI_RULE_MODE
    CHECK (RULE_MODE IN ('CUMULATIVE_DWELL', 'SUSTAINED_FIXATION', 'FIXATION_COUNT', 'HYBRID')),

  CONSTRAINT CHK_AOI_RULE_THRESHOLDS
    CHECK (
      (MIN_DWELL_MS IS NULL OR MIN_DWELL_MS >= 0) AND
      (MIN_FIXATION_COUNT IS NULL OR MIN_FIXATION_COUNT >= 0) AND
      (MIN_SUSTAINED_FIXATION_MS IS NULL OR MIN_SUSTAINED_FIXATION_MS >= 0) AND
      (MAX_ALLOWED_GAP_MS IS NULL OR MAX_ALLOWED_GAP_MS >= 0) AND
      (REQUIRED_ORDER_NO IS NULL OR REQUIRED_ORDER_NO >= 1)
    )
);

