CREATE TABLE FLIGHT_SESSION (
  SESSION_ID                VARCHAR2(64) PRIMARY KEY,
  SCENARIO_ID               VARCHAR2(100),
  PILOT_ID                  VARCHAR2(100),
  SESSION_STATUS            VARCHAR2(30) DEFAULT 'ACTIVE' NOT NULL,
  STARTED_AT                TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  ENDED_AT                  TIMESTAMP,
  SCREEN_WIDTH              NUMBER,
  SCREEN_HEIGHT             NUMBER,
  GRID_COLS                 NUMBER DEFAULT 40 NOT NULL,
  GRID_ROWS                 NUMBER DEFAULT 24 NOT NULL,
  TOTAL_SAMPLES             NUMBER DEFAULT 0 NOT NULL,
  TOTAL_FIXATIONS           NUMBER DEFAULT 0 NOT NULL,
  TOTAL_GAZE_DURATION_MS    NUMBER DEFAULT 0 NOT NULL,
  NOTES                     VARCHAR2(500),

  CONSTRAINT CHK_FLIGHT_SESSION_STATUS
    CHECK (SESSION_STATUS IN ('ACTIVE', 'COMPLETED', 'ABORTED'))
);

CREATE TABLE GAZE_SAMPLE (
  SAMPLE_ID                 NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  SESSION_ID                VARCHAR2(64) NOT NULL,
  TS_MS                     NUMBER NOT NULL,
  ELAPSED_MS                NUMBER,
  X                         NUMBER(10,2) NOT NULL,
  Y                         NUMBER(10,2) NOT NULL,
  NX                        NUMBER(8,4),
  NY                        NUMBER(8,4),
  AOI                       VARCHAR2(64),
  CONFIDENCE                NUMBER(5,4),
  GRID_X                    NUMBER,
  GRID_Y                    NUMBER,
  IS_VALID                  NUMBER(1) DEFAULT 1 NOT NULL,
  INVALID_REASON            VARCHAR2(100),
  CREATED_AT                TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,

  CONSTRAINT FK_GAZE_SAMPLE_SESSION
    FOREIGN KEY (SESSION_ID) REFERENCES FLIGHT_SESSION(SESSION_ID),

  CONSTRAINT CHK_GAZE_SAMPLE_VALID
    CHECK (IS_VALID IN (0, 1)),

  CONSTRAINT CHK_GAZE_SAMPLE_CONFIDENCE
    CHECK (CONFIDENCE IS NULL OR (CONFIDENCE >= 0 AND CONFIDENCE <= 1))
);

CREATE TABLE GAZE_FIXATION (
  FIXATION_ID               NUMBER GENERATED ALWAYS AS IDENTITY PRIMARY KEY,
  SESSION_ID                VARCHAR2(64) NOT NULL,
  AOI                       VARCHAR2(64),
  START_TS_MS               NUMBER NOT NULL,
  END_TS_MS                 NUMBER NOT NULL,
  DURATION_MS               NUMBER NOT NULL,
  CENTER_X                  NUMBER(10,2),
  CENTER_Y                  NUMBER(10,2),
  CENTER_NX                 NUMBER(8,4),
  CENTER_NY                 NUMBER(8,4),
  GRID_X                    NUMBER,
  GRID_Y                    NUMBER,
  SAMPLE_COUNT              NUMBER DEFAULT 0 NOT NULL,
  IS_VERIFIED_LOOK          NUMBER(1) DEFAULT 0 NOT NULL,
  VERIFY_RULE_ID            VARCHAR2(64),
  CREATED_AT                TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,

  CONSTRAINT FK_GAZE_FIXATION_SESSION
    FOREIGN KEY (SESSION_ID) REFERENCES FLIGHT_SESSION(SESSION_ID),

  CONSTRAINT CHK_GAZE_FIXATION_VERIFY
    CHECK (IS_VERIFIED_LOOK IN (0, 1)),

  CONSTRAINT CHK_GAZE_FIXATION_TIME
    CHECK (END_TS_MS >= START_TS_MS),

  CONSTRAINT CHK_GAZE_FIXATION_DURATION
    CHECK (DURATION_MS >= 0)
);

CREATE TABLE AOI_AGGREGATE (
  SESSION_ID                VARCHAR2(64) NOT NULL,
  AOI                       VARCHAR2(64) NOT NULL,
  SAMPLE_COUNT              NUMBER DEFAULT 0 NOT NULL,
  FIXATION_COUNT            NUMBER DEFAULT 0 NOT NULL,
  TOTAL_DWELL_MS            NUMBER DEFAULT 0 NOT NULL,
  AVG_FIXATION_MS           NUMBER DEFAULT 0 NOT NULL,
  LONGEST_FIXATION_MS       NUMBER DEFAULT 0 NOT NULL,
  FIRST_LOOK_TS_MS          NUMBER,
  LAST_LOOK_TS_MS           NUMBER,
  LOOK_VERIFIED_COUNT       NUMBER DEFAULT 0 NOT NULL,
  LAST_UPDATED              TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,

  CONSTRAINT PK_AOI_AGGREGATE
    PRIMARY KEY (SESSION_ID, AOI),

  CONSTRAINT FK_AOI_AGG_SESSION
    FOREIGN KEY (SESSION_ID) REFERENCES FLIGHT_SESSION(SESSION_ID),

  CONSTRAINT CHK_AOI_AGG_TIMES
    CHECK (
      FIRST_LOOK_TS_MS IS NULL
      OR LAST_LOOK_TS_MS IS NULL
      OR LAST_LOOK_TS_MS >= FIRST_LOOK_TS_MS
    )
);

CREATE TABLE HEATMAP_CELL_AGGREGATE (
